// ilk ders olarak ekrana yazı yazdırma 
Console.WriteLine("Merhaba Dünya");
Console.Write("Merhaba Dünya");
//iki satırın çıktısı aynı olacak tek farkı "WriteLine" methodu kullanılan satırdan sonra 
// aşşağı satıra geçer "Write" methodunda ise aynı satırdan devam eder
// bu kodu sade bu şekilde kullanmaya bilirsiniz mesela üzerinde matematik işlemleri yaptırabilirsiniz
Console.WriteLine(5 + 5);
// bu kodun çıktısında iki 5 rakamını toplayıp 10 çıktısını verecektir
// ama +(artı) semboli sadece toplamak için kullanılmıyor yan yana eklemek içinde kullanılıyor
Console.WriteLine("5" + "5");
// mesele buı kodda 5 rakamlkarı "(çivt tırnak) içinde olduğu için 55 sonucunu gösterecek
// ve ilerde göreceğimiz olumlu ve olumsuz olarak kullanılan true,false türünüde burda yazdırabiliriz
Console.WriteLine(10 > 0);
// burdada 10 0'dan büyükmü diye soru sordum büyük olduğu için true dönecektir
// anlayacağınız metin yazarken "(çivt tırnak) semboli içinden yazılır
